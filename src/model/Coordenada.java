package model;

/**
 *
 * @author ricardobalduino
 */
public class Coordenada {

    /**
     *
     */
    private int x;

    /**
     *
     */
    private int y;

    /**
     *
     * @param x
     */
    public void setX(int x){
        this.x = x;
    }
    
    /**
     *
     * @param y
     */
    public void setY(int y){
        this.y = y;
    }
    
    /**
     *
     * @return
     */
    public int getX(){
        return x;
    }

    /**
     *
     * @return
     */
    public int getY() {
        return y;
    }
    
    
}
